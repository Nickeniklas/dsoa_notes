public class Clothing {

    String brand;
    String price;
    String type;

    //  GETTERS
    public String getBrand() {
        return brand;
    }
    public String getPrice() {
        return price;
    }
    public String getType() {
        return type;
    }

    //  SETTERS
    public void setBrand(String brand) {
        this.brand = brand;
    }
    public void setPrice(String price) {
        this.price = price;
    }
    public void setType(String type) {
        this.type = type;
    }
}
