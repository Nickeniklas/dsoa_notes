public class Clothing {

    String brand;
    int price = 0;
    String type;

    //  GETTERS
    public String getBrand() {
        return brand;
    }
    public int getPrice() {
        return price;
    }
    public String getType() {
        return type;
    }
    //  SETTERS
    public void setBrand(String brand) {
        this.brand = brand;
    }
    public void setPrice(int price) {
        this.price = price;
    }
    public void setType(String type) {
        this.type = type;
    }
}
